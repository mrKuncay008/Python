# Operasi Aritmatika yang menampilkan 
# Diketahui : Nilai a = 31, b = 7, c = 29, d = 12 
# Berdasarkan nilai tersebut, buatlah bentuk operator aritmatikanya menggunakan python

a = 31
b = 7
c = 29 
d = 12

jumlah = a + b + c + d #penjumlhan
kurang = a - b - c - d #pengkurangan
kali = a * b * c * d #pengkalian
bilbulat = kali // a #pembagian
pangkat = b ** 2 #pangkat

print("Hasil penjumlahan = ", jumlah)
print("Hasil pengurangan = ", kurang)
print("Hasil pengkalian= ", kali)
print("Hasil pembagian = ", bilbulat)
print("Hasil pangkat = ", pangkat)

